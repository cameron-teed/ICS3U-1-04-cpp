// This program calculates the area and perimeter of a rectangle with the
//     dimensions 5cm x 5cm


# include <iostream>

int main()  {
    std::cout <<"If a rectangle has the dimensions:"<< std::endl;
    std::cout <<"5cm x 5cm"<< std::endl;
    std::cout <<""<< std::endl;
    std::cout <<"Area is "<< (5*5) << "cm^2" << std::endl;
    std::cout <<"Perimeter is " << (2*(5+5)) << "cm" << std::endl;
}
